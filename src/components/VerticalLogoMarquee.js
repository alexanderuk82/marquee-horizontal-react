const VerticalLogoMarquee = () => {
  const marqueeRef = useRef(null);
  
  // Duplicar los logos para un efecto continuo
  const duplicatedLogos = [...logos, ...logos];

  return (
    <div className="vertical-marquee-container">
      <div className="marquee-wrapper">
        <motion.div 
          className="marquee"
          animate={{ 
            y: [0, -50 * logos.length] 
          }}
          transition={{ 
            repeat: Infinity,
            repeatType: "loop",
            duration: 20,
            ease: "linear"
          }}
          ref={marqueeRef}
        >
          {duplicatedLogos.map((logo, index) => (
            <div className="logo-item" key={`${logo.id}-${index}`}>
              <div className="logo-content">
                {logo.svg}
                <span className="logo-name">{logo.name}</span>
              </div>
            </div>
          ))}
        </motion.div>
      </div>
    </div>
  );
};

